# Простое сложение
2 + 3  
# [1] 5  (R выведет результат сложения, здесь 5)

# Присвоение результата переменной с помощью оператора <-
sum_result <- 2 + 3   
# Теперь в переменной sum_result хранится значение 5

# Вычитание и умножение с сохранением результатов
diff_result <- 10 - 4
# 10 минус 4, результат 6 сохраняется в diff_result
prod_result <- 7 * 5    # 7 умножить на 5, результат 35 сохраняется в prod_result

# Деление и возведение в степень
div_result <- 20 / 4    # 20 разделить на 4, результат 5
pow_result <- 2^3       # 2 в степени 3 (возведение в степень), результат 8

# Выведем значения переменных, чтобы убедиться в результатах
sum_result    # [1] 5
diff_result   # [1] 6
prod_result   # [1] 35
div_result    # [1] 5
pow_result    # [1] 8
