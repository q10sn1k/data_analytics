
# Построить столбчатую диаграмму продаж по 4 товарам.
# Это классическая категориальная визуализация: на оси X — категории,
# на оси Y — числовые значения (объём продаж).


# СОЗДАЁМ МАЛЕНЬКИЙ НАБОР ДАННЫХ (4 СТРОКИ)
# Вектор названий товаров (категории)
products <- c("Ноутбук", "Планшет", "Телефон", "Наушники")

# Вектор продаж (числовые значения), такое же количество элементов
sales <- c(35, 22, 50, 18)

# (Не обязательно, но удобно держать всё в одной таблице)
df_sales <- data.frame(Product = products, Sales = sales)

# СМОТРИМ, ЧТО ПОЛУЧИЛОСЬ (быстрая проверка)
df_sales
str(df_sales)

# СТРОИМ СТОЛБЧАТУЮ ДИАГРАММУ
# barplot() — базовая функция для столбчатых диаграмм.
# Аргумент 'names.arg' — подписи под столбцами (на оси X).
# 'main' — заголовок, 'xlab'/'ylab' — подписи осей.
# 'col' — цвет столбиков (можно один цвет на все столбцы).
barplot(
  height = df_sales$Sales,            # высота столбцов = значения продаж
  names.arg = df_sales$Product,       # подписи категорий под столбцами
  main = "Продажи по категориям",     # заголовок графика
  xlab = "Категория товара",          # подпись оси X
  ylab = "Продажи, шт.",              # подпись оси Y
  col  = "steelblue"                  # цвет столбиков
)

# СЕТКА ПО ОСИ Y (необязательно, но помогает читать значения)
# abline(h = ...) рисует горизонтальные линии. 'lty=3' — пунктир.
abline(h = pretty(df_sales$Sales), lty = 3, col = "gray70")
