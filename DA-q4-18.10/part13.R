
# Построить линейный график посещаемости сайта по дням (5 точек).
# Это временной ряд из 5 наблюдений: на оси X — дни, на оси Y — визиты.


# МАЛЕНЬКИЙ ДАТАСЕТ (5 СТРОК)
days <- c("Пн", "Вт", "Ср", "Чт", "Пт")    # 5 категорий (дни недели)
visits <- c(120, 150, 130, 160, 180)       # 5 чисел (кол-во визитов)

df_visits <- data.frame(Day = days, Visits = visits)
df_visits
str(df_visits)

# СТРОИМ ЛИНЕЙНЫЙ ГРАФИК
# ВАЖНО: plot() по умолчанию отрисует точки; чтобы соединить линией,
# используем type="o" (overplotted — и точки, и линия)
# 'xaxt="n"' — отключаем автоматические подписи на оси X,
# чтобы поставить свои (русские сокращения дней).
plot(
  x = 1:nrow(df_visits),           # позиция точек по X: 1,2,3,4,5
  y = df_visits$Visits,            # значения по Y
  type = "o",                      # точки + соединяющая линия
  main = "Посещаемость по дням",
  xlab = "День недели",
  ylab = "Визиты, чел.",
  xaxt = "n",                      # не рисуем стандартные подписи X
  col = "darkred",                 # цвет линии/точек
  pch = 16,                        # форма точки (16 — заполненный кружок)
  lwd = 2                          # толщина линии
)

# ДОБАВЛЯЕМ СВОИ ПОДПИСИ ДНЕЙ НЕДЕЛИ НА ОСЬ X
axis(side = 1, at = 1:nrow(df_visits), labels = df_visits$Day)

# ДОПОЛНИТЕЛЬНО: СЕТКА ПО Y ДЛЯ УДОБСТВА ЧТЕНИЯ
abline(h = pretty(df_visits$Visits), lty = 3, col = "gray70")
