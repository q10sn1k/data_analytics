# Построить scatter plot (диаграмму рассеяния) для оценки связи
# между временем подготовки (часы) и результатом теста (балл).
# Каждая точка — один студент. Легко увидеть тенденцию.


# МАЛЕНЬКИЙ ДАТАСЕТ (5 СТРОК)
student <- c("Аня", "Борис", "Вика", "Глеб", "Даша")
hours   <- c(2, 4, 3, 5, 1)      # часы подготовки
score   <- c(65, 78, 72, 90, 55) # итоговый балл

df_exam <- data.frame(Student = student, Hours = hours, Score = score)
df_exam
str(df_exam)

# СТРОИМ ТОЧКИ: X = ЧАСЫ, Y = БАЛЛ
plot(
  x = df_exam$Hours,
  y = df_exam$Score,
  main = "Связь результата с временем подготовки",
  xlab = "Часы подготовки",
  ylab = "Баллы за тест",
  pch = 19,            # маркер точки (19 — плотный круг)
  col = "darkgreen"
)

# (НЕОБЯЗАТЕЛЬНО) ДОБАВИМ ЛИНЕЙНЫЙ ТРЕНД
# lm(y ~ x) — линейная регрессия, abline() — линия на график.
model <- lm(Score ~ Hours, data = df_exam)
abline(model, col = "blue", lwd = 2)

# (НЕОБЯЗАТЕЛЬНО) ПОДПИШЕМ ИМЕНА РЯДОМ С ТОЧКАМИ
# 'pos' управляет позиционированием подписи: 1-ниже,2-левее,3-выше,4-правее
text(
  x = df_exam$Hours,
  y = df_exam$Score,
  labels = df_exam$Student,
  pos = 3, cex = 0.9, col = "gray20"
)
