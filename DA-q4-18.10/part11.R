# Генерация 100 случайных чисел из нормального распределения (среднее=0, стандартное отклонение=1)
random_data <- rnorm(100, mean = 0, sd = 1)

# Построение гистограммы распределения случайных данных
hist(random_data, 
     breaks = 10,                    # разбить диапазон на 10 интервалов (столбиков)
     main = "Гистограмма случайных данных", 
     xlab = "Значения", 
     col = "skyblue", 
     border = "black")
