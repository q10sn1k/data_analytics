# Создадим вектор из нескольких чисел
numbers <- c(10, 20, 15, 5, 30)  
# Функция c() объединяет указанные числа в вектор

# Выведем созданный вектор
numbers  
# [1] 10 20 15  5 30  (вектор из 5 чисел)

# Вычислим сумму всех элементов вектора
total <- sum(numbers)  
total  
# [1] 80  (10+20+15+5+30 = 80)

# Вычислим среднее арифметическое элементов вектора
avg <- mean(numbers)  
avg  
# [1] 16  (среднее значение равно 16)

# Найдём максимальное и минимальное значения вектора
max_val <- max(numbers)  
min_val <- min(numbers)  
max_val  # [1] 30  
min_val  # [1] 5
