# Сравнить распределение времени доставки (в днях) между тремя способами:
# "Курьер", "Почта", "Самовывоз".
# ВАЖНО: датасет крошечный (5 наблюдений), поэтому boxplot носит
# скорее учебный характер (для иллюстрации), но он всё равно строится.


# МАЛЕНЬКИЙ ДАТАСЕТ (5 СТРОК)
Method <- c("Курьер", "Курьер", "Почта", "Почта", "Самовывоз")
Days   <- c(2,        3,         5,       4,        1)

df_ship <- data.frame(Method, Days)
df_ship
str(df_ship)

# СТРОИМ BOXPLOT:
# Формула вида Y ~ X означает: рисуем распределение Y по группам X.
# Здесь: Days ~ Method — "ящики" времени доставки для каждого способа.
boxplot(
  Days ~ Method,
  data = df_ship,
  main = "Время доставки по способу (ящики с усами)",
  xlab = "Способ доставки",
  ylab = "Время, дни",
  col  = c("lightblue", "lightgreen", "moccasin")
)

# ПОДСКАЗКА ПО ИНТЕРПРЕТАЦИИ:
# - Линия в «ящике» — медиана.
# - Верх/низ «ящика» — 3-й и 1-й квартили.
# - «Усы» — диапазон без выбросов.
# - При очень малом числе наблюдений форма «ящика» может быть атипичной,
#   вплоть до отдельных точек (это нормально для малленького набора данных).
